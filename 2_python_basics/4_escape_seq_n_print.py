# Escape sequences and print statement in Python
# Escape sequences in Python
print("Hello\nWorld")     # \n = new line
print("Tab\tspace")       # \t = tab space
print("He said \"Hi\"")   # \" = double quote
print('It\'s okay')       # \' = single quote
print("Backslash: \\ hhh")    # \\ = backslash('\')
print("Unicode: \u03A9")  # \u = unicode character (Î©)


# Print statement usage
print("Python", "is", "awesome")              # Comma separates values with space
print("Python" + " is " + "awesome")          # + concatenates strings directly
print("Stay", end=" ")                        # end prevents newline
print("Strong!")                              # continues on same line

# Note:
# Escape sequences let you insert special characters in strings.
# print() is used to display output.
