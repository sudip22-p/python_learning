"""
✅ Python Indentation (Spacing)

- Indentation means giving space before code lines.
- Python uses indentation to show which code is **inside** something (like a function or loop).
- Usually, we use **4 spaces**.
"""

# ✔️ Correct example
def say_hello():
    print("Hello")  # This is indented → inside the function

say_hello()

# ❌ Wrong example (this gives error)
# def say_hi():
# print("Hi")  # ❌ No indentation → Python will show IndentationError
