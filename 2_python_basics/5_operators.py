# Arithmetic Operators
a = 10
b = 3
print(a + b)  # Addition: 13
print(a - b)  # Subtraction: 7
print(a * b)  # Multiplication: 30
print(a / b)  # Division: 3.333...
print(a // b) # Floor Division: 3
print(a % b)  # Modulus: 1
print(a ** b) # Exponentiation: 1000

# Comparison Operators
print(a == b)  # Equal: False
print(a != b)  # Not Equal: True
print(a > b)   # Greater: True
print(a < b)   # Less: False
print(a >= b)  # Greater or Equal: True
print(a <= b)  # Less or Equal: False

# Logical Operators
x = True
y = False
print(x and y)  # Logical AND: False
print(x or y)   # Logical OR: True
print(not x)    # Logical NOT: False

# Assignment Operators
c = 5
c += 2  # Equivalent to c = c + 2
print(c)  # Output: 7

# Bitwise Operators
m = 4     # 0100
n = 5     # 0101
print(m & n)  # AND: 4
print(m | n)  # OR: 5
print(m ^ n)  # XOR: 1
print(~m)     # NOT: -5
print(m << 1) # Left Shift: 8
print(m >> 1) # Right Shift: 2

# Membership Operators
list1 = [1, 2, 3]
print(2 in list1)     # True
print(4 not in list1) # True

# Identity Operators
print(a is b)       # False
print(a is not b)   # True

